
# -----------------------------------------------------------------------------
# Load libraries/data/etc.
# -----------------------------------------------------------------------------
library(tidyverse)

df <- read.csv("soccer.csv") 


# -----------------------------------------------------------------------------
# Split data
# -----------------------------------------------------------------------------
train_perc <- 0.8

set.seed(25644)
values <- sample(1:nrow(df), train_perc * nrow(df), replace = FALSE)

train <- df[values,]
test <- df[-values,]


# -----------------------------------------------------------------------------
# Write results
# -----------------------------------------------------------------------------
write_csv(train, "train.csv")
write_csv(test, "test.csv")
